public class Conditional {
    
    public void fln(int a, int b, int c) {
        if (a > b && a > c) {
            System.out.println("A is the largest number");
        }else if (b > c) {
            System.out.println("B is the largest number");
        }else {
            System.out.println("C is the largest number");
        }

    }
}
